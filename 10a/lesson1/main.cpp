char *Malloc(size_t size)
{
    char *arr = new char[size];

    return arr;
}

char *Realloc(const char *const src, size_t size)
{
    char *arr = new char[size];
    for (size_t i = 0; i < size; i++)
    {
        arr[i] = src[i];
    }
    delete[] src;
    return arr;
}

